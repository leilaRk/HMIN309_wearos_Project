public class MainActivity extends WearableActivity {

    LinearLayout linearLayout, ambientLayout;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        this.requestWindowFeature(Window.FEATURE_NO_TITLE);
        setContentView(R.layout.activity_main);
        setAmbientEnabled();
        linearLayout = findViewById(R.id.mainLayout);
        ambientLayout = findViewById(R.id.mainAmbientLayout);
    }

    public void sendMessageHandler(View view){
        Intent intent = new Intent(this, Send_Activity.class);
        startActivity(intent);
    }

    public void viewMessageListHandler(View view){
        Intent intent = new Intent(this, ShowMessagesActivity.class);
        startActivity(intent);
    }

    @Override
    public void onEnterAmbient(Bundle ambientDetails) {
        super.onEnterAmbient(ambientDetails);
        linearLayout.setVisibility(View.GONE);
        ambientLayout.setVisibility(View.VISIBLE);
    }

    @Override
    public void onUpdateAmbient() {
        super.onUpdateAmbient();
    }

    @Override
    public void onExitAmbient() {
        super.onExitAmbient();
        linearLayout.setVisibility(View.VISIBLE);
        ambientLayout.setVisibility(View.GONE);
    }
}
